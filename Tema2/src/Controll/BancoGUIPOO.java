    private void btBorrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btBorrarMouseClicked
        // TODO add your handling code here:
       int filaSeleccionada=jTable1.getSelectedRow();
       int filaSeleccionadaCorriente=jTable2.getSelectedRow();
       if(filaSeleccionada>=0){
           modelo.removeRow(filaSeleccionada);
       }else{
           JOptionPane.showMessageDialog(null, "Tabla vacia o no selecciono una fila");
       }
       if(filaSeleccionadaCorriente>=0){
           modelo2.removeRow(filaSeleccionadaCorriente);
       }else{
           JOptionPane.showMessageDialog(null, "Tabla vacia o no selecciono una fila");
       }
    }//GEN-LAST:event_btBorrarMouseClicked

    private void btEliminarTodoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEliminarTodoActionPerformed
       int cantidadFilas = jTable1.getRowCount();
       int cantidadFila2 = jTable1.getRowCount();
       for(int i=cantidadFila2-1;i>=0;i--){
           modelo.removeRow(i);
       }
       for(int i=cantidadFilas-1;i>=0;i--){
           modelo2.removeRow(i);
       }
    }//GEN-LAST:event_btEliminarTodoActionPerformed

