    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked

        String []DatosCuentaAhorros =new String[4];
        String []DatosCuentaCorriente =new String[4];
        
         if(brAhorro.isSelected()){
            DatosCuentaAhorros[0]=txNum.getText();
            txNum.setText(null);
            DatosCuentaAhorros[1]=txNom.getText();
            txNom.setText(null);
            DatosCuentaAhorros[2]=txSal.getText();
            txSal.setText(null);
            DatosCuentaAhorros[3]=brAhorro.getText();
            brAhorro.setText(null);
            txCla.setText(null);
            modelo.addRow(DatosCuentaAhorros);
            JOptionPane.showMessageDialog(null, "CUENTA CREADA CON EXITO");
         }
          if(brCorriente.isSelected()){
            DatosCuentaAhorros[0]=txNum.getText();
            txNum.setText(null);
            DatosCuentaAhorros[1]=txNom.getText();
            txNom.setText(null);
            DatosCuentaAhorros[2]=txSal.getText();
            txSal.setText(null);
            DatosCuentaAhorros[3]=brCorriente.getText();
            brCorriente.setText(null);           
            txCla.setText(null);
            modelo2.addRow(DatosCuentaCorriente);
            JOptionPane.showMessageDialog(null, "CUENTA CREADA CON EXITO");
         }           
    }//GEN-LAST:event_jButton1MouseClicked

