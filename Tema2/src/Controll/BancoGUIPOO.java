    private void brCorrienteStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_brCorrienteStateChanged
        // Evento cambio de estado de los boton de radio Corriente
         if(brAhorro.isSelected()){  //si esta seleccionado Ahorro, los oculta
            lbGiro.setVisible(false);
            txGiro.setVisible(false);
        }
         if(brCorriente.isSelected()){ //si esta seleccionado corriente, los muestra
            lbGiro.setVisible(true);
            txGiro.setVisible(true);
         }
    }//GEN-LAST:event_brCorrienteStateChanged

